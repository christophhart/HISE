String SyntaxTreeExtractor::getBase64DataLayout(const Array<ValueTree>& dataLayouts)
{
    MemoryOutputStream mos;
    
    for(const auto& x: dataLayouts)
    {
        zstd::ZDefaultCompressor comp;
        
        MemoryBlock mb;
        comp.compress(x, mb);
        
        mos.writeInt((int)mb.getSize());
        mos.write(mb.getData(), mb.getSize());
    }
    
    mos.flush();
    return "b64" + mos.getMemoryBlock().toBase64Encoding();
}

Array<juce::ValueTree> SyntaxTreeExtractor::getDataLayoutTrees(const juce::String &b64)
{
    Array<ValueTree> list;
    
    jassert(b64.startsWith("b64"));
    
    MemoryBlock mb;
    mb.fromBase64Encoding(b64.substring(3));
    MemoryInputStream mos(std::move(mb));
    
    while(!mos.isExhausted())
    {
        int numBytes = mos.readInt();
        
        MemoryBlock mb;
        mb.ensureSize(numBytes);
        mos.read(mb.getData(), numBytes);
        
        zstd::ZDefaultCompressor comp;
        
        ValueTree v;
        comp.expand(mb, v);
        
        list.add(v);
    }
    
    return list;
}


void InitValueParser::forEach(const std::function<void(uint32 offset, Types::ID type, const VariableStorage& value)>& f) const
{
	if (!input.startsWith("b64"))
	{
		InitValueParser p(getB64());
		p.forEach(f);
		return;
	}

	auto d = input.substring(3);

	MemoryBlock mb;
	mb.fromBase64Encoding(d);
	MemoryInputStream mos(std::move(mb));

	int offset = 0;

	while (!mos.isExhausted())
	{
		auto b = mos.readByte();

		switch (b)
		{
		case 'i':
			f(offset, Types::ID::Integer, VariableStorage(mos.readInt()));
			offset += sizeof(int);
			break;
		case 'f':
			f(offset, Types::ID::Float, VariableStorage(mos.readFloat()));
			offset += sizeof(float);
			break;
		case 'd': 
			
			if (offset % sizeof(double) != 0)
				offset += sizeof(double) - (offset % sizeof(double));

			f(offset, Types::ID::Double, VariableStorage(mos.readDouble()));

			offset += sizeof(double);
			break;
		case 'p':

			if (offset % sizeof(void*) != 0)
				offset += sizeof(void*) - (offset % sizeof(void*));

			f(offset, Types::ID::Pointer, VariableStorage(reinterpret_cast<void*>(mos.readInt64()), 8));
			offset += sizeof(void*);
			break;
		case 'I':
			f(offset, Types::ID::Integer, VariableStorage((void*)(uint64_t)mos.readInt()));
			offset += sizeof(int);
			break;
		case 'F':
			f(offset, Types::ID::Float, VariableStorage((void*)(uint64_t)mos.readInt()));
			offset += sizeof(float);
			break;
		case 'D':

			if (offset % sizeof(double) != 0)
				offset += sizeof(double) - (offset % sizeof(double));

			f(offset, Types::ID::Double, VariableStorage((void*)(uint64_t)mos.readInt()));
			offset += sizeof(double);

			break;
		default: jassertfalse; return;
		}
	}
}

juce::uint32 InitValueParser::getNumBytesRequired() const
{
	uint32 b = 0;

	forEach([&b](uint32 offset, Types::ID t, const VariableStorage&)
	{
		auto nb = (uint32)Types::Helpers::getSizeForType(t);

		if (t == Types::ID::Integer)
			nb = sizeof(int64);

		b = offset + nb;
	});

	return b;
}

String InitValueParser::dumpContent() const
{
	String s;

	forEach([&s](int offset, Types::ID type, const VariableStorage& v)
	{
		auto typestring = Types::Helpers::getTypeName(type);

		if (v.getType() == Types::ID::Pointer)
			typestring = "expr";

		s << typestring << ":" << Types::Helpers::getCppValueString(v) << " ";
	});

	return s;
	//s << "float: " << mos.readFloat() << ", "; break;
}

String InitValueParser::getB64() const
{
	MemoryOutputStream mos;

	jassert(!input.startsWith("b64"));
	

	auto ptr = input.begin();
	auto end = input.end();

	while (ptr != end)
	{
		auto c = *ptr;

		if (CharacterFunctions::isWhitespace(c) ||
			c == '{' || c == '}' || c == ',')
		{
			ptr++;
			continue;
		}

		auto isExpression = false;

		uint8_t expressionType = 0;

		if (*ptr == '$')
		{
			isExpression = true;
			ptr++;
			expressionType = *ptr++;
		}

		bool isMinus = *ptr == '-';

		if (isMinus)
			ptr++;

		auto b = ptr;
		auto e = ptr;

		while (e != end &&
			    (CharacterFunctions::isDigit(*e) ||
				*e == '.' ||
				*e == 'f' ||
                *e == 'x' ||
			    *e == 'p'))
		{
			e++;
		}

		String value(b, e);

        if(value.isEmpty())
            break;
        
		if (isExpression)
		{
			mos.writeByte(CharacterFunctions::toUpperCase(expressionType));
			mos.writeInt(value.getIntValue());
		}
		else
		{
			auto type = Types::Helpers::getTypeFromStringValue(value);

			if (type == Types::ID::Integer)
			{
				mos.writeByte('i');
				mos.writeInt(value.getIntValue() * (!isMinus ? 1 : -1));
			}
			if (type == Types::ID::Float)
			{
				mos.writeByte('f');
				mos.writeFloat(value.getFloatValue() * (!isMinus ? 1.0f : -1.0f));
			}
			if (type == Types::ID::Double)
			{
				mos.writeByte('d');
				mos.writeDouble(value.getDoubleValue() * (!isMinus ? 1.0 : -1.0));
			}
			if (type == Types::ID::Pointer)
			{
				mos.writeByte('p');

				auto v2 = value.substring(3);
				auto hv = v2.getHexValue64();

				mos.writeInt64(hv);
			}
		}

		ptr = e;
	}

	mos.flush();
	return "b64" + mos.getMemoryBlock().toBase64Encoding();
}

