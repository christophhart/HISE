int Compiler::compileCount = 0;

 void Compiler::reset()
 {
	 if (compiler != nullptr)
		 delete compiler;

	 handler = new NamespaceHandler();
	 compiler = new ClassCompiler(&memory, *handler);
	 memory.registerFunctionsToNamespaceHandler(getNamespaceHandler());
 }

 bool Compiler::allowInlining() const
 {
	 return memory.getOptimizationPassList().contains(OptimizationIds::Inlining);
 }

 snex::jit::FunctionClass::Map Compiler::getFunctionMap()
 {
	 auto m = memory.getMap();

	 if (auto ifc = getInbuiltFunctionClass())
		 m.addArray(ifc->getMap());
	 
	 for (auto t : handler->getComplexTypeList())
	 {
		 FunctionClass::Ptr fc = t->getFunctionClass();
		 auto fm = fc->getMap();

		 m.addArray(fm);
	 }

	 return m;
 }


Compiler::Compiler(GlobalScope& memoryPool):
	memory(memoryPool),
	cr(Result::ok())
{
	reset();
}

Compiler::~Compiler()
{
	if(compiler != nullptr)
		delete compiler;
}


