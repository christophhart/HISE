    NamespaceHandler::SymbolDebugInfo di;
	compiler->namespaceHandler.addSymbol(s, TypeInfo(v.getType(), true, false), NamespaceHandler::Constant, di);
	compiler->namespaceHandler.addConstant(s, v);
