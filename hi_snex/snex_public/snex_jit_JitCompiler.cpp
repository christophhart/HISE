juce::ValueTree SyntaxTreeExtractor::getSyntaxTree(const String& b64)
{
	ValueTree v;

	if (isBase64Tree(b64))
	{
		static const String prefix("B64Tree::");
		MemoryBlock mb;
		mb.fromBase64Encoding(b64.substring(prefix.length()));

		zstd::ZCompressor<SyntaxTreeDictionaryProvider> comp;

		auto ok = comp.expand(mb, v);
		jassert(ok.wasOk());
	}

	return v;
}

String SyntaxTreeExtractor::getBase64SyntaxTree(ValueTree v)
{
	removeLineInfo(v);

	String b64;

	if (v.isValid())
	{
		b64 << "B64Tree::";
		MemoryBlock mb;
		zstd::ZCompressor<SyntaxTreeDictionaryProvider> comp;
		auto ok = comp.compress(v, mb);
		jassert(ok);

		b64 << mb.toBase64Encoding();
	}

	return b64;
}

bool SyntaxTreeExtractor::isBase64Tree(const String& code)
{
	static const String prefix("B64Tree::");
	return code.startsWith(prefix);
}

