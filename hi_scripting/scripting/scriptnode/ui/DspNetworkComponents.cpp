			PopupMenu m;
			m.setLookAndFeel(&plaf);

			auto s = g.network->getSelection().getFirst();

			m.addItem((int)NodeComponent::MenuActions::WrapIntoDspNetwork, "Wrap into DSP Network", NodeComponent::PopupHelpers::isWrappable(s) != 0);

